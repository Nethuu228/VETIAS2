// FOR LOOP
console.log("FOR LOOP");
for(let i = 1; i <= 10; i++) {
    console.log("2 * " + i + " = " + (2 * i));
}

// WHILE LOOP
console.log("WHILE LOOP");
let j = 1;
while(j <= 10) {
    console.log("2 * " + j + " = " + (2 * j));
    j++;
}
// DO WHILE LOOP
console.log("DO WHILE LOOP");
let k = 1;
do {
    console.log("2 * " + k + " = " + (2 * k));
    k++;
} while(k <= 10);